#include <stdio.h>

int main() {
    //Operadores Relacionais
    // > Maior que;
    // < Menor que;
    // >= Maior ou igual a;
    // <= Menor ou igual a;
    // == Igual a;
    // != Diferente de;

    float a = 10.2;
    int b = 10;

    printf("a > b: %d\n", (int)a > b);
    printf("a <= b: %d\n", a <= b);
    printf("a == b: %d\n", a == b);
    printf("a != b: %d\n\n", a != b);

    return 0;
}